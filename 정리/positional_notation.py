# 2진수 0b1101 = 10진수 1*8 + 1*4 + 0*2 + 1*1 = 13
# 8진수 0o23 = 10진수 2*8 + 3*1 = 19
# 16진수 0x12 = 10진수 16*1 + 2*1 = 18

print(int("0b1101", 2) == 13)
print(int("0o23", 8) == 19)
print(int("0x12", 16) == 18)

n = 19
oct = ""
while n:
    q, r = n // 8, n % 8
    oct += str(r)
    n = q
print(int("0o" + oct[::-1], 8) == 19)
